// What is oop? List oop concepts.
// -------Object-oriented programming (OOP) is a programming style that uses objects, 
// which are made up of data and code, instead of logic-based systems.
// 
// ---------oop concepts--------------------------------------------------------------------------------------------------------
// Encapsulation:- Encapsulation is a process of wrapping the data into a single entitiy.
//
//-----> Which provides security to the data.
// 
//There are two methods setter and getter.
//
//---------> Setter is used to set the data  and getter is used to get the data.
//-------------------------------------------------------------------------------------------------
//
//Inheritance:-
//
//when one class inherits the properties of another class is called Inheritance.
//
//parent class:-Base class:- Super class
//child class:-Derived class:-Sub class
//
//Child class will inherits all the properties of parent class.
//
//
//There are 5 types of Inheritance:-
//
//1)Single-level Inheritance:-
//
//		class A
//		   |
//		class B
//
//2)Multi-level Inheritance:-
//
//		class A
//		   |
//		class B
//		   |
//		class C
//
//3)Multiple Inheritance:-
//
//		class A    class B
//			|		|
//		     class C
//
//4)Hierarchical Inheritance:- 
//	
//							 class A             
//							 |      |
//					  class B       class C	
//					 |    |         |      |
// 			  class D  class E    class F  class G
// 			  
//5)Hybrid Inheritance:-
//							class A
//							|      |
//					 class B 	  class C
//							|	   |
//							class D		  
//
//------------------------------------------------------------------------------------------------
//
//Abstraction:- Abstraction is a process to hide unneccesary information from the user and only show neccessary part to the user.
//
//---> It shows only neccessary implementation to the user.
//
//Abstraction has two types: -
//
//1) Abstraction through header files
//
//2) Abstraction through class
//
//------------------------------------------------------------------------------------------------
//
//Polymorphism:- Polymorphism derives from greek word. Poly means many and morphism means forms. So polymorphism means many forms.
//
//---> Polymorphism is defined as a class having many forms.
//
//There are two types of polymorphism:-
//
//1) Method overloading: When class having same method name but different parameters is called Method Overloading.
//
//2) Method overriding: When parent class and child class having same method name is called Method Overriding.
//
//------------------------------------------------------------------------------------------------
//Inline Function:-
//
//Inline is a special type of function which is used to increase the execution speed of the program.
//
//---> It increases execution speed by replacing the function declaration by function calling.
//
//---> To use inline function we have to use inline keyword.
//
//------------------------------------------------------------------------------------------------
//
//Friend Function:-
//
//----->Friend Function is used to call the privte and protected members outside the class.
//
//----->To used friend functions you have to use friend keyword.
//------------------------------------------------------------------------------------------------.
//
//Protected:-
//
//-----> Protected is a access specifire which will call within the class and by its derived class.
//
//-------------------------------------------------------------------------------------------------

//Templates:-
//
//Templates is a generic programming which can be used to multiple datatypes is a single class or function.
//
//We can use multiple datatypes in a single  class or function using templates.
//
//For using templates you have to add template keyword.
//
//-------------------------------------------------------------------------------------------------

//What is the difference between OOP and POP? 
//
//Focus: OOP focuses on data security, while POP focuses on completing tasks.
//
//Access modes: OOP has three access modes: "Public", "Private", and "Protected", 
//while POP has no specific access modes.
//
//Inheritance: POP doesn't support inheritance, while OOP does.
//
//Virtual functions: In OOP, the virtual function supports polymorphism, 
//while there is no concept of virtual function in POP.
//Code reusability: OOP allows code reuse, while POP does not.
//
//Structure orientation: POP is more structure-oriented than object, unlike OOPs.
//
//Programming approach: Procedural programming follows a top-down approach,
// while object-oriented programming follows a bottom-up approach.
